package cn.codekong.bean.label;

public class AssignImageLabel {
	
	private String finished_img;
	private String unfinished_img;
	private String all_img_amount;
	
	public String getFinished_img() {
		return finished_img;
	}
	public void setFinished_img(String finished_img) {
		this.finished_img = finished_img;
	}
	public String getUnfinished_img() {
		return unfinished_img;
	}
	public void setUnfinished_img(String unfinished_img) {
		this.unfinished_img = unfinished_img;
	}
	public String getAll_img_amount() {
		return all_img_amount;
	}
	public void setAll_img_amount(String all_img_amount) {
		this.all_img_amount = all_img_amount;
	}
	

}
